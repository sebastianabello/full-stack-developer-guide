//BOM
//El BOM significa Browser Object Model, es un objeto que contiene todas las propiedades y métodos del navegador web
//El objeto window es el objeto global del navegador, representa la ventana del navegador que contiene el DOM
//El objeto window tiene métodos para abrir, cerrar y manipular una ventana, y también tiene métodos para el historial del navegador
//El objeto window es compatible con todos los navegadores modernos
//Propiedades

//window.innerHeight - devuelve la altura interior de la ventana del navegador (en píxeles)
console.log(window.innerHeight);
//window.innerWidth - devuelve el ancho interior de la ventana del navegador (en píxeles)
console.log(window.innerWidth);
//window.outerHeight - devuelve la altura exterior de la ventana del navegador (en píxeles)
console.log(window.outerHeight);
//window.outerWidth - devuelve el ancho exterior de la ventana del navegador (en píxeles)
console.log(window.outerWidth);

//window.location - devuelve el objeto ubicación
console.log(window.location);
//window.location.href - devuelve la URL completa de la página actual
//window.location.hostname - devuelve el nombre de dominio de la dirección web
